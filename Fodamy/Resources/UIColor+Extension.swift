import UIKit

extension UIColor {
    
    static var appFocus: UIColor {
        return UIColor(named: "appFocus")!
    }
    
    static var appText: UIColor {
        return UIColor(named: "appText")!
    }
    
    static var appButtonRed: UIColor {
        return UIColor(named: "appButtonRed")!
    }
    
    static var appWhite: UIColor {
        return UIColor(named: "appWhite")!
    }
}
